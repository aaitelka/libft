#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*cdst;
	char	*csrc;

	cdst = (char *)dst;
	csrc = (char *)src;
	if (csrc < cdst)
		while (len--)
			cdst[len] = csrc[len];
	else
		ft_memcpy(dst, src, len);
	return (dst);
}
